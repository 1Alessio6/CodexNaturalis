package it.polimi.ingsw.model.card.Color;

/**
 * Enumeration representing the possible colors of the cards.
 */
public enum CardColor {

    /**
     * RED, if the card is red
     */
    RED,

    /**
     * GREEN, if the card is green
     */
    GREEN,

    /**
     * BLUE, if the card is blue
     */
    BLUE,

    /**
     * YELLOW, if the card is yellow
     */
    YELLOW,

    /**
     * PURPLE, if the card is purple
     */
    PURPLE
}
